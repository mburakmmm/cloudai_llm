    def sync_train(self, prompt: str, response: str, intent: str = None, tags: list = None, priority: int = 1, category: str = "genel") -> bool:
        """Senkron eƒüitim metodu"""
        try:
            # Giri≈ü kontrol√º
            if not self.is_meaningful_input(prompt) or not self.is_meaningful_input(response):
                logger.warning("Anlamsƒ±z giri≈ü tespit edildi")
                return False
                
            # Vekt√∂r hesapla
            try:
                prompt_embedding = self.encode_text(prompt)
                # PyTorch tens√∂r√ºn√º NumPy dizisine d√∂n√º≈üt√ºr
                if torch.is_tensor(prompt_embedding):
                    prompt_embedding = prompt_embedding.cpu().numpy()
            except Exception as e:
                logger.error(f"Vekt√∂r hesaplama hatasƒ±: {str(e)}")
                return False
                
            # Belleƒüe ekle
            try:
                memory_data = {
                    "prompt": prompt,
                    "response": response,
                    "embedding": prompt_embedding,
                    "intent": intent or "genel",
                    "tags": tags or [],
                    "priority": priority,
                    "category": category,
                    "created_at": datetime.now().isoformat()
                }
                
                memory_id = self.memory_manager.add_memory(memory_data)
                logger.info(f"Yeni bellek eklendi: {memory_id}")
                
                # Supabase'e kaydet
                training_data = {
                    "prompt": prompt,
                    "response": response,
                    "intent": intent or "genel",
                    "tags": tags or [],
                    "priority": priority,
                    "category": category,
                    "confidence_score": 1.0,
                    "created_at": datetime.now().isoformat()
                }
                
                result = self.supabase.table('training_data').insert(training_data).execute()
                
                if not result.data:
                    raise Exception("Supabase'e veri eklenemedi")
                    
                logger.info(f"Eƒüitim verisi ba≈üarƒ±yla eklendi - ID: {result.data[0]['id']}")
                return True
                
            except Exception as e:
                logger.error(f"Bellek/Supabase kayƒ±t hatasƒ±: {str(e)}")
                return False
                
        except Exception as e:
            logger.error(f"Eƒüitim verisi eklenirken hata: {str(e)}")
            return False

